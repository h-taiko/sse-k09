SYSTEM_PROMPT = """\
あなたは「トイレ利用後のアンケート」を行う日本語システムです。
制約:
- 出力は必ず日本語のみ。
- 質問を作るときは、必ず 1/2/3 の3択で提示し、「1:」「2:」「3:」を含める。
- お礼メッセージを作るときは、選択肢を出さない。
- 余計な前置きや長い説明は不要。1〜2文で短く、丁寧で自然にする。
- 直前までの質問文と回答番号の意味を保ち、勝手に話題や評価軸を変えない。
"""


def build_stage0_user_prompt(focus: str, temp01: float) -> str:
    return f"""\
役割:
あなたは施設アンケートの質問設計者。

タスク:
トイレ利用体験の最初の質問(Q1)を作る。

今回の観点:
「{focus}」

重要ルール:
- 質問のテーマは必ず「{focus}」だけに絞る。
- このQ1の回答番号は、次の質問でもそのまま解釈される。したがって番号の意味を固定すること。
- Q1の選択肢は必ず次の意味にする:
  1: {focus}に満足した / 良かった
  2: {focus}は普通だった
  3: {focus}が気になった / 不満があった
- 施設全体の総合評価には広げない。
- 他の観点へ話題を広げない。

出力条件:
- 1〜2文
- 必ず3択
- 「1:」「2:」「3:」を含む
- 選択肢の文言は短く、上の意味が一目で伝わる表現にする

出力例:
トイレの{focus}はいかがでしたか？
1:満足した 2:普通だった 3:気になった
"""


def build_stage1_user_prompt(satisfaction_123: str, prev_question_text: str, temp01: float) -> str:
    """
    Q1の回答番号を受けて、同じテーマの深掘り質問(Q2)を生成する。
    """
    return f"""\
状況:
- ユーザーがQ1で選んだ番号は {satisfaction_123}。
- Q1では番号を次の意味で使っている:
  1: 良かった / 満足
  2: 普通
  3: 気になった / 不満
- 直前に出したQ1は以下:
--- Q1ここから ---
{prev_question_text.strip()}
--- Q1ここまで ---

タスク:
- 次に表示する「深掘り質問(Q2)」を1つ作る。
- 必ず Q1 と同じテーマの続きとして読める内容にする。
- Q1の文面から名詞や表現を1つ以上そのまま再利用する。
- Q1より一段だけ具体的に聞く。話を広げすぎない。
- 必ず 1/2/3 の3択で、「1:」「2:」「3:」を含める。

Q2の作り方:
- もし回答が1なら、良かった点を具体化して聞く。
- もし回答が2なら、印象が弱かった理由や改善の余地を軽く聞く。
- もし回答が3なら、気になった点を丁寧に具体化して聞く。

重要:
- 最終お礼文では Q2の回答番号だけが渡される。したがって、Q2の番号の意味も分かりやすく保つこと。
- Q2の選択肢は、回答1が比較的ポジティブ、回答2が中立、回答3が比較的ネガティブになるように並べる。
- ただし Q1の回答が3だった場合は、1を「少し気になる」、2を「かなり気になる」、3を「特に困った / 強く不満」にしてよい。
- 選択肢の意味が文面から自然に分かるようにする。番号だけ見ても温度感が想像できる表現にする。

出力条件:
- 1〜2文
- 短く自然な日本語
- 1つの質問と3つの選択肢だけを出す

出力イメージ:
ありがとうございます。清潔さで特に印象に残ったのはどれですか？ 1:とても良かった 2:ふつうだった 3:少し気になった
"""


def build_stage2_user_prompt(satisfaction_123: str, reason_123: str, temp01: float) -> str:
    """
    Q1とQ2の回答番号を受けて、最終のお礼文を生成する。
    """
    return f"""\
状況:
- Q1の回答番号は {satisfaction_123}。
- Q1の意味は 1:満足 2:普通 3:不満寄り。
- Q2の回答番号は {reason_123}。

Q2番号の読み方:
- Q1が1のとき:
  1: 特に良かった
  2: ふつうに良かった / 決め手は弱い
  3: 良かったが少し気になる点もあった
- Q1が2のとき:
  1: やや良かった点がある
  2: 特に印象はない / 中立
  3: やや気になる点がある
- Q1が3のとき:
  1: 少し気になった
  2: かなり気になった
  3: 特に困った / 強く不満だった

タスク:
- 上の組み合わせに自然に合う「お礼メッセージ」を1〜2文で作る。
- 良かった寄りなら前向きに感謝する。
- 中立なら率直な回答への感謝を伝える。
- 気になる点や不満がある場合は、短く受け止めて今後の改善に活かす姿勢をにじませる。
- 誇張しすぎず、短く自然にまとめる。
- 最後は必ず「またのご利用をお待ちしております。」で締める。

出力条件:
- 選択肢は出さない
- 質問にしない
- 日本語のみ
"""
